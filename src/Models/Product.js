class Product {
    constructor(productId, name, imageUrl, price, description, categoryId, inProcess, createdDate, userId) {
        this.productId = productId;
        this.name = name;
        this.imageUrl = imageUrl;
        this.price = price;
        this.description = description;
        this.categoryId = categoryId;
        this.inProcess = inProcess;
        this.createdDate = createdDate;
        this.userId = userId;
    }
}